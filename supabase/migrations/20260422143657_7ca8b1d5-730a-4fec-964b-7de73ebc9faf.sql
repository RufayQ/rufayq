### 3 · اختيار وجهتك — إطار عمل بـ 5 خطوات

1. **حددي احتياجاتك الطبية.** تجميل → تركيا، تايلاند، كوريا الجنوبية. قلب/أورام → ألمانيا، سويسرا. التلقيح الصناعي → تركيا، ماليزيا، إسبانيا. عظام → ألمانيا، تركيا، ماليزيا. أسنان → تركيا، المجر. سمنة → المكسيك، تايلاند، تركيا.

2. **تحققي من اعتماد المستشفى.** أصري على **اعتماد JCI (لجنة الاعتماد الدولية)** بالإضافة إلى التسجيل الحكومي المحلي. تحققي من المراجعات المستقلة ومعدلات الإعادة والمضاعفات المنشورة.

3. **تحققي من بيانات الجراح.** اسألي: *كم مرة أجرى الجراح هذا الإجراء المحدد بالضبط؟* (الهدف: 500+). ما هي معدلات المضاعفات المنشورة؟ هل سيتنسقون ما بعد الجراحة مع طبيبك السعودي؟

4. **أكدي الشفافية المالية.** اطلبي عرض أسعار مفصل: رسوم الجراح، رسوم المرفق، التخدير، الغرسات، الأدوية، التصوير ما بعد الجراحة، النقل. الأسعار "الموحدة" الغامضة سبب للخروج.

5. **اختبري الاستجابة.** اطرحي سؤالاً سريرياً واحداً مفصلاً. رد مفصل طبياً في 24 ساعة ينبئ بنفس جودة التنسيق لاحقاً.

