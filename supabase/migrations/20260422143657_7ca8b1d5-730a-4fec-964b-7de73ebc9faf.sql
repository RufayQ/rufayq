### ما كانت ستقول فاطمة

إذا كنتِ تقرئي هذا لأنكِ أسبوعين إلى ثلاثة أسابيع بعيدة عن جراحة بالخارج وأدركتِ للتو أنكِ تحتاجين إلى ترجمة سجلاتك الطبية، فإن فاطمة كانت ستقول لكِ هذا:

> "لا تفعلي ما فعلته. لا تذعري. لا تستخدمي Google Translate. لا تنتظري حتى اللحظة الأخيرة.
>
> الطريقة الهجينة — ماسح الذكاء الاصطناعي بالإضافة إلى مراجعة مترجم معتمد — هي الطريقة الوحيدة للقيام بهذا بشكل صحيح. إنه سريع. إنه دقيق. إنه في متناول الجميع.
>
> أنفقت 250 ريال و 4 أيام على ترجمة صحيحة. إذا أنفقت 0 ريال و ساعتين على Google Translate، كنت سأنفق 5,000+ ريال وأسبوعين على حلول الطوارئ.
>
> الاختيار يبدو واضحاً الآن. لم يكن واضحاً لي في ذلك الوقت.
>
> اتخذي الاختيار الصحيح الآن. لا تكوني مثلي."

