### الاكتشاف: كيف حل طبيب سعودي واحد مشكلة الترجمة

شرحت د. الراشد شيئاً غير الكل شيء.

"الترجمة الطبية ليست مثل ترجمة قائمة مطعم،" قالت. "عندما تترجمي المعنى السريري، كلمة واحدة خاطئة يمكن أن تغير كل شيء."

أعطت فاطمة مثالاً من تقريرها الخاص.

"رأيتِ حيث يقول 'ER+/PR+'؟" سألت.

"نعم،" قالت.

"هذا يعني أن سرطانك حساس للعلاج الهرموني. هذا حاسم جداً. لكن إذا ترجمه شخص بدون خلفية طبية كلمة بكلمة، قد يكتبون شيئاً مثل 'مستقبل للعلاج' وهو غامض ويفقد المعنى المحدد."

أدركت فاطمة أن تقريرها المترجم بـ Google Translate ربما كان يحتوي على عشرات هذه الأخطاء.

"فماذا أفعل الآن؟" سألت بيأس.

شرحت د. الراشد شيئاً يُدعى "الطريقة الهجينة".

"يمكنك استخدام ماسح الوثائق الذكي للذكاء الاصطناعي لاستخراج وتنظيم سجلاتك الطبية،" قالت. "يقرأ العربية المكتوبة بخط اليد، يرقمنها، ويستخرج بيانات المفتاح الطبي. ثم يراجع مترجم طبي معتمد ما أنتجه الذكاء الاصطناعي — لا يترجم من الصفر، لكن يتحقق من الدقة. هذا يكلف أقل، يأخذ وقتاً أقل، ودقيق أكثر من أي من الطريقتين وحدها."

"كم من الوقت؟" سألت فاطمة.

"4-5 أيام عمل،" قالت. "والتكلفة حوالي 150-250 ريال بدلاً من 300-400 ريال لمترجم كامل."

شعرت فاطمة بأول لحظة أمل في ساعات.

