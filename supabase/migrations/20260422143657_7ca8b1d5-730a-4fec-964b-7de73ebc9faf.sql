### ذعر الـ 48 ساعة: عندما انهار كل شيء

اتصلت فاطمة بطبيبتها الأورام السعودية، د. الراشد، وهي تبكي.

"تم إلغاء الجراحة،" قالت. "الطبيب الألماني لن يجري العملية حتى يكون لديه ترجمات صحيحة. لكن لا يمكنني الحصول على مترجم معتمد في الوقت المناسب. هذه جراحتي الرابعة للسرطان. لا أستطيع الانتظار لمدة شهر آخر."

استمعت د. الراشد. رأت هذا من قبل. "فاطمة،" قالت ببطء، "المشكلة ليست أن المترجمين الطبيين المعتمدين غير موجودين. المشكلة هي أنك فعلت ما يفعله 1 من كل 3 مريضات سرطان سعودية — حاولت ترجمة المستندات الطبية بنفسك باستخدام أدوات غير مصممة للطب."

"ماذا كان يمكنني أن أفعل بشكل مختلف؟" سألت فاطمة بصوت متشقق.

"دعيني أريكِ،" قالت د. الراشد.

