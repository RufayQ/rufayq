*محدث أبريل 2026 · قراءة 15 دقيقة · بقلم د. عبدالرحمن مرسي*

كانت الساعة الثالثة صباحاً في الرياض عندما ضغطت فاطمة على "إرسال".

قضت كل المساء تترجم تقرير الباثولوجيا الخاص بها باستخدام Google Translate. رحلتها إلى ميونخ تغادر خلال 72 ساعة. لم يكن لديها خيار آخر.

التقرير يحتوي على ثلاث صفحات. تقارير الباثولوجيا معقدة. Google Translate ليس معقداً بالطريقة الصحيحة.

شاهدت وهي تراقب وكيف "الخباثة" أصبحت "الشرارة".

شاهدت وكيف "التصنيف النقيلي" تحول إلى "شدة انتشار المرض".

شاهدت وكيف تم إدراج علامات الأورام الحرجة — القيم التي ستحدد ما إذا كانت ستعيش أم تموت — بوحدات لا توجد في المعايير الطبية الألمانية.

لكنها كانت متعبة. كانت مرعوبة. وكان لديها جراحة للاستعداد لها.

أرسلت البريد الإلكتروني إلى مكتب الدكتور شميدت في ميونخ.

