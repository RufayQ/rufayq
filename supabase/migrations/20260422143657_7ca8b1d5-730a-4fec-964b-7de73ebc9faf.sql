### 1 · لماذا يختار السعوديون السياحة العلاجية

السعودية تشهد طفرة في السياحة العلاجية — لكن ليس لأن الرعاية الصحية السعودية ضعيفة. أربعة عوامل تدفع هذا الاتجاه:

**1. الإجراءات المتخصصة ذات فترات الانتظار الطويلة محلياً.** استبدال مفصل العظام في السعودية: قائمة انتظار 6-12 شهر. ألمانيا: 2-3 أسابيع. بالنسبة لشخص بعمر 45 سنة فشل مفصله، هذا ليس خياراً — إنها ضرورة.

**2. توفير التكاليف للإجراءات غير المغطاة.** التلقيح الصناعي في الرياض: 20,000-40,000 ريال. تركيا: 8,000-12,000 ريال بنفس النتائج والاعتماد الدولي.

**3. الوصول إلى الخبرة النادرة.** بعض الحالات القلبية، العظمية والسرطانية لديها فقط 2-3 مستشفيات عالمياً بخبرة عميقة.

**4. التحقق من الرأي الثاني.** سيسعى أي مريض سعودي يتم تشخيصه بحالة نادرة إلى رأي ثانٍ من مركز معروف عالمياً.

