### 10 · رحلات مرضى سعوديين حقيقية

**فاطمة، 42 — إعادة بناء الركبة (ميونخ).** تمزق غضروفي، قائمة انتظار سعودية 6 أشهر. إصلاح منظار جراحي ميونخ: 90 دقيقة، شق <0.5 سم. متابعة عن بُعد منسقة مع طبيبتها السعودية. العودة للتنس بحلول الشهر 4. **الإجمالي: 52,000 ريال.** *"استعدت حياتي."*

**أحمد وزوجته — التلقيح الصناعي (إسطنبول).** ثلاث دورات فاشلة في الرياض. بروتوكول دورة طبيعية في إسطنبول: حمل ناجح في الدورة الأولى. **الإجمالي (الإجراء + الرحلات + الإقامة): 18,000 ريال.** توأم أولاد، أصحاء، 12 شهراً.

**ريان، 58 — قسطرة قلبية (برلين).** متلازمة القلب الحادة. كانت مستشفى Charité برلين متاحة في نفس اليوم. قسطرة + دعامة. إعادة تشغيل مشتركة من قبل Charité وطبيب قلب سعودي. العودة للعمل في 4 أسابيع. **الإجمالي: 85,000 ريال.** *"أنقذوا حياتي، وطبيبي السعودي أنقذ تعافي."*

