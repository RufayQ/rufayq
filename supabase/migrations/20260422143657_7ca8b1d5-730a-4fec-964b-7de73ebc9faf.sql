### 5 · أثناء العلاج — التنسيق والتواصل

**اليوم الأول (الوصول).** أكدي فحوصات/تصوير ما قبل الجراحة. التقي الجراح، أخصائي التخدير، منسق التمريض. اطرحي ثلاثة أسئلة حرجة:

- *"هل ستوجهي ملخص الخروج مباشرة إلى طبيبي السعودي؟"*
- *"من أتصل به بعد الخروج؟"*
- *"ما هو بروتوكول ما بعد الجراحة؟"*

**اليوم السابق للجراحة.** أنهي توقف/بدء الأدوية. التقي بأخصائي التخدير — ناقشي الحساسيات وتاريخ التخدير العائلي. إحاطة أخيرة عن موقع الشق وجدول التعافي.

**الأيام 1-7 بعد الجراحة.** لا تعاني من الألم في صمت. راقبي الشقوق بحثاً عن احمرار/دفء/إفراز. اتبعي قواعم حركة العلاج الطبيعي *بالضبط*. صوري الشق يومياً، سجلي درجات الألم وأوقات الأدوية — هذا يصبح ملخص الخروج.

**قبل الخروج (اليوم 5-7).** احصلي على **ملخص خروج كامل**، وصفات لـ ≥ أسبوعين، جدول المتابعة (أسبوعان → 6 أسابيع → 3 أشهر)، والتزام الجراح بالمتابعة البعيدة.

