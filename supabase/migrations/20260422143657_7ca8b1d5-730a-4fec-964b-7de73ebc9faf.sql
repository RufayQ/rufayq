- متوسط التأخير: 7-14 يوم
- متوسط تكلفة التأخيرات: 5,000-15,000 ريال في اختبارات إضافية وإقامة ممتدة

لكن ما لا يعرفه معظم المرضى: هناك طريقة أفضل.

