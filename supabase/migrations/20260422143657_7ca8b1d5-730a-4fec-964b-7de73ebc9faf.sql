### بعد 72 ساعة: عندما وصل البريد الإلكتروني إلى ألمانيا

فتح الدكتور شميدت صندوق بريده صباح يوم الخميس. ملفات مريضة من امرأة سعودية. جراحة مجدولة ليوم الاثنين.

قرأ الصفحة الأولى من تقرير الباثولوجيا.

ثم توقف.

قرأها مرة أخرى.

اتصل بمترجمه الطبي.

"ماذا تعني 'خباثة السرطان'؟" سأل.

"هذا لا معنى له،" قال المترجم. "هذا ليس مصطلحاً طبياً."

شعر الدكتور شميدت بقشعريرة.

نظر إلى علامات الأورام. الوحدات خاطئة. القيم لا تتطابق مع نطاقات المختبر الألمانية القياسية. لم يكن يعرف ما إذا كان الورم بالمرحلة الثانية أم الرابعة.

نظر إلى ملخص تقرير التصوير. وقد ذكر "شدة انتشار المرض" لكنه لم يحدد أين أو كم.

اتخذ قراراً.

اتصل برقم فاطمة (الذي أدرجته في البريد الإلكتروني، مرعوبة، تطلب التأكيد).

"يا سيدة فاطمة،" قال بحذر. "استقبلت سجلاتك الطبية. أحتاج إلى التحدث مع طبيبك السعودي مباشرة. أحتاج إلى أن يراجع الطبيب لوحة الباثولوجيا الأصلية بواسطة مترجم طبي معتمد. لا يمكنني إجراء العملية يوم الاثنين حتى أفهم الباثولوجيا تماماً."

شعرت معدة فاطمة بالاستسلام.

كانت لديها 72 ساعة. سيحتاج المترجم إلى 7-10 أيام. سيكلف مترجم طبي معتمد أموالاً لا تملكها.

تم إلغاء الجراحة.

