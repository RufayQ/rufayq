### الحل الحديث: التكنولوجيا تلتقي بالطب

هناك فئة جديدة من التطبيقات مصممة خصيصاً لمسافرين طبيين مثل فاطمة.

تطبيقات تفهم أن ترجمة المستندات الطبية ليست مثل ترجمة قائمة مطعم.

تطبيقات تجمع بين ماسح وثائق الذكاء الاصطناعي مع الإشراف الطبي البشري.

تطبيقات تضيف نطاقات مرجعية تلقائياً لقيم المختبر.

تطبيقات تضع علم على التناقضات ("أنتِ تدرجين حساسية من البنسلين *و* الأموكسيسيلين — هذان متفاعلان بشكل متبادل").

تطبيقات تنسق بين طبيبك السعودي وجراحك الأجنبي.

أفضلها تفعل كل هذا في أقل من 10 دقائق مقابل 50 ريال بدلاً من 300+ ريال الذي يفرضه المترجمون التقليديون.

رُفَيِّق هو أحد هذه التطبيقات. تم بناؤه خصيصاً لمرضى سعوديين مثل فاطمة — مرضى يحتاجون إلى ترجمة سجلات طبية بدقة وسرعة قبل السفر للعلاج بالخارج.

