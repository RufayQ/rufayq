### الحل: ما نجح فعلاً

خلال 24 ساعة، كانت د. الراشد قد مرّت فاطمة من خلال العملية.

أولاً، جمعوا سجلها الطبي الكامل:

- ملخص طبيبة الأورام السعودية السريري (السياق لكل شيء)
- تقرير الباثولوجيا (بالعربية، الأصل الذي ماسحته)
- تقارير التصوير من مستشفاها السعودية
- فحوصات دمها من ثلاثة أشهر مضت
- قائمة أدويتها
- سجل حساسيتها

"لماذا كل هذا؟" سألت فاطمة.

"لأن جراحك الألماني يحتاج إلى رؤية الصورة الكاملة،" شرحت د. الراشد. "إذا أرسلنا تقرير الباثولوجيا فقط، يجب عليهم ملء الفراغات بأنفسهم. هذا ينشئ التباس."

استخدموا تطبيق ماسح وثائق ذكي للرقمنة الملاحظات العربية المكتوبة بخط اليد من ملف عيادة فاطمة السعودية. في دقائق، كان التطبيق قد:

- قرأ الخط (حتى الملاحظات التي يصعب قراءتها)
- استخرج البيانات الطبية تلقائياً
- قدم ترجمة إنجليزية أولية
- وضع علم على المناطق التي تحتاج إلى مراجعة بشرية

ثم وصلت د. الراشد فاطمة بمترجمة طبية معتمدة — شخص يتمتع بـ 15 سنة من الخبرة في ترجمة حالات الأورام على وجه التحديد.

"لا أترجم من الصفر،" شرحت المترجمة عندما التقيا. "أنا أراجع ما أنتجه الذكاء الاصطناعي وأتحقق من الدقة الطبية. هذا أسرع بكثير من الترجمة من الصفر."

قضت المترجمة ساعتين في مراجعة عمل الذكاء الاصطناعي، تصحيح المصطلحات السريرية، والتأكد من أن علامات الأورام لها وحدات ونطاقات مرجعية مناسبة، والتأكد من أن أكواد التشخيص تطابق المعايير الطبية الألمانية.

كانت الوثيقة المترجمة النهائية بدقة 95% — مقارنة بدقة 40% محاولة Google Translate لفاطمة.

التكلفة الإجمالية: 200 ريال.

الوقت الإجمالي: 4 أيام.

كتبت د. الراشد رسالة تغطية للدكتور شميدت شخصياً:

> "الدكتور شميدت،
>
> أكتب لأقدم لك مريضتي فاطمة، التي أعالجها من سرطان الثدي منذ 2023. أرفقت سجلاتها الطبية الكاملة مترجمة إلى الألمانية.
>
> النتائج الرئيسية: Grade 2 ductal carcinoma, ER+/PR+ (حساسة للهرمونات)، HER2-سالبة. لا توجد مشاركة الغدد اللمفاوية المكتشفة على التصوير. مناسبة للعلاج الهرموني مدمج مع الجراحة.
>
> تسافر المريضة إلى مؤسستك للتدخل الجراحي. الوثائق المرفقة شاملة ومترجمة بدقة. أنا متاح لأي توضيحات مطلوبة.
>
> مع أطيب التحيات،
>
> د. الراشد"

استقبل الدكتور شميدت البريد الإلكتروني صباح السبت.

قرأ رسالة التغطية.

قرأ تقرير الباثولوجيا المترجم بشكل صحيح.

قرأ ملخص التصوير بمصطلحات صحيحة.

اتصل بـ فاطمة مباشرة.

"يا سيدة فاطمة،" قال، "لقد راجعت سجلاتك الطبية الكاملة. كل شيء واضح. جراحتك مؤكدة للاثنين كما هو مجدول في الأصل."

