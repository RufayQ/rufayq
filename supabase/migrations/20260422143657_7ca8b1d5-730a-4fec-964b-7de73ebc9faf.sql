### 9 · 10 أعلام حمراء يجب أن تجعلك تتركي

1. لا اعتماد JCI.
2. تسعير غامض ("اتصلي للحصول على عرض أسعار").
3. لا توجد معدلات مضاعفات منشورة.
4. ضغط للقرار السريع.
5. لا موظفين متحدثين للإنجليزية أو مترجم.
6. المستشفى لن تلتزم بالتنسيق ما بعد الجراحة.
7. الجراح أجرى الإجراء أقل من 500 مرة.
8. لا توجد سياسة رد/إعادة واضحة.
9. شهادات فقط من موقع المستشفى الخاص.
10. ضغط لاستخدام تمويل في الموقع محفوف بالمخاطر.

