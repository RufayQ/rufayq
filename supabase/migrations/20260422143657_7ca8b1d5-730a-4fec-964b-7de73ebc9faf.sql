### 6 · العودة الحرجة — التنسيق مع طبيبك السعودي

هنا حيث ينهار النظام. تعودي إلى الوطن بعد الجراحة، منهكة وهشة — وطبيبك السعودي ليس لديه أي رؤية لما تم فعله. **الحل: تسليم منظم.** للحصول على دليل عميق لهذه المرحلة، اقرأي [دليل تنسيق التعافي بعد العلاج](/ar/news/recovery-coordination-post-treatment-care).

**الأسبوع الأول في السعودية.** حددي موعد زيارة شخصية أو فيديو مع طبيبك السعودي. احضري ملخص الخروج (مترجم)، الصور/مقاطع الفيديو الجراحية إن وجدت، الأدوية الحالية، فحوصات الدم الأجنبية، قيود النشاط، ومعلومات اتصال الجراح الأجنبي.

ناقشي:

1. **المصالحة الدوائية** — التفاعلات مع أدويتك المزمنة.
2. **جدول النشاط** — متى يمكنك استئناف العمل والقيادة والرياضة؟
3. **توقعات الأعراض** — ما هو الانتفاخ/الألم/الإحساس بالصلابة الطبيعي؟
4. **بروتوكولات الطوارئ** — حمى، ألم مفاجئ، إفراز → خطة العمل.
5. **المتابعة المشتركة** — التصوير المنسق بعد 6 أسابيع و 3 أشهر مع الجراح الأجنبي.

**قوس التعافي.** الأسابيع 1-2 = حاد (ألم، انتفاخ، إرهاق، القشرة طبيعية). الأسابيع 2-4 = الحركة (ذروة الانتفاخ اليوم 10 ثم تنخفض، العلاج الطبيعي يشتد، الأنشطة الخفيفة تستأنف).

**بروتوكول التسليم.** قبل أن ينقطع جراحك، اطلبي: بروتوكول مكتوب ما بعد الجراحة، جدول تصوير، سلطة صريحة لطبيبك السعودي للتصرف، وتقويم متابعة افتراضي (أسبوعان → 6 أسابيع → 3 أشهر → حسب الحاجة). هذا يحول *التسليم* إلى **شراكة**.

