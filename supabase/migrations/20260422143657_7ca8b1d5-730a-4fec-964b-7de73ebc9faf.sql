### 4 · قائمة التحقق قبل السفر بـ 60 يوماً

**الأسابيع 1-2 — التحضير الطبي**

- اجمعي كل التصوير (CT/MRI/X-rays مع نسخ رقمية)، فحوصات الدم، قائمة الأدوية، سجل الحساسية، السجل الجراحي.
- اجمعيها في ملف PDF واحد وأرسليها إلى منسق المستشفى الأجنبية.
- احصلي على ترجمات طبية معتمدة (50-150 ريال لكل وثيقة، 5-7 أيام عمل). للتعمق، راجعي [دليل ترجمة الوثائق الطبية](/ar/news/medical-document-translation-ai-scanning).
- أنشئي قياسات أساسية: ضغط الدم، معدل ضربات القلب أثناء الراحة، الوزن، جودة النوم، الألم الحالي.

**الأسابيع 3-4 — اللوجستيات**

- الرحلات الجوية: يوم 1-2 قبل الجراحة (2,000-4,000 ريال درجة الدرجة السياحية).
- الإقامة: 14-21 ليلة (100-300 ريال/ليلة فندق التعافي).
- **احضري شخصاً مرافقاً.** غير قابل للتفاوض لأي إجراء كبير.
- التقي بطبيبك السعودي *شخصياً* واطلبي **ملخص سريري** مكتوب للفريق الأجنبي.
- أخطري شركة التأمين — أكدي التغطية و**بند إعادة الإجلاء** كتابياً. معظم التأمين السعودي *لا يغطي* السياحة الاختيارية.

**الأسابيع 5-6 — التأكيد النهائي**

- فحوصات دم ما قبل الجراحة (محلياً في السعودية، مرسلة قبل الرحيل بأسبوع).
- تعديلات الأدوية (توقفي الأسبرين؟ استمري في حاصرات بيتا؟).
- اطلبي مسبقاً أدوية ما بعد الجراحة من صيدليتك السعودية *قبل* الرحيل.
- اطبعي مجلداً فيزيائياً: جواز سفر، فيزا طبية، تأمين، جهات اتصال مستشفى + طبيب سعودي، سجلات، أدوية، حساسيات.

