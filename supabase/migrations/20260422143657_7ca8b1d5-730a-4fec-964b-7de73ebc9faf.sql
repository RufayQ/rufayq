### الاثنين: عندما حدثت الجراحة في الوقت المحدد

استيقظت فاطمة في الساعة 6 صباحاً من يوم الاثنين في غرفة فندقها في ميونخ.

كانت مرعوبة. لكن ليس خوفاً من الجراحة نفسها.

كانت ممتنة لأن طبيبتها أراها كيفية فعل هذا بشكل صحيح.

بعد ثلاثة أسابيع، بعد جراحة ناجحة وتعافي، اتصلت فاطمة بـ د. الراشد.

"سارت الجراحة بنجاح تام،" قالت. "قال لي د. شميدت أن الوثائق المترجمة كانت واضحة جداً، دقيقة جداً، بحيث لم يكن لديه سؤال واحد عن الباثولوجيا أو السجل الطبي. قال إنه نادراً ما يرى سجلات طبية منظمة بهذه الطريقة ومترجمة بشكل جيد من المريضات الدوليات."

"هذا ما يحدث عندما تفعلينها بشكل صحيح في المرة الأولى،" قالت د. الراشد.

