### تكلفة القيام به بشكل خاطئ مقابل القيام به بشكل صحيح

**إذا فعلتيها بشكل خاطئ (مثل فاطمة في البداية):**

- محاولة Google Translate: 0 ريال، ساعتان، دقة 40%
- النتيجة: تم إلغاء الجراحة
- مترجم معتمد إضافي مطلوب: 300-400 ريال، 7-10 أيام
- تصوير إضافي للتأكيد: 3,000-5,000 ريال
- تكاليف فندق للجراحة المؤجلة: 2,000-3,000 ريال
- تكلفة عاطفية: لا تُقدر بثمن
- **التكلفة الإجمالية: 5,300-8,400 ريال + أسبوعين تأخير**

**إذا فعلتيها بشكل صحيح (الطريقة الهجينة):**

- ماسح وثائق الذكاء الاصطناعي: 50 ريال
- مراجعة مترجم معتمد: 200 ريال
- تنسيق الطبيب: 0 ريال
- إرسال منظم: 0 ريال
- النتيجة: تتم الجراحة في الموعد المحدد، بدون التباس
- **التكلفة الإجمالية: 250 ريال + بدون تأخير**

**الفرق: 5,000 ريال+ و 14 يوم.**

لجراحة 50,000 ريال، هذا استثمار 250 ريال من الطريقة الهجينة يبلغ 0.5% من التكلفة الإجمالية.

إنه تأمين. وهو غير قابل للتفاوض.

