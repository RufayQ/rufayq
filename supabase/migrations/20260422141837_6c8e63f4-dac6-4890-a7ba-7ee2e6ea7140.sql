**Step 4 — Physician coordination.** Your Saudi physician writes a cover letter to your foreign surgeon explaining context.

**Step 5 — Organised transmission.** Send documents in batches: clinical summary → pathology → imaging → labs. Request confirmation. Provide a backup link.

