### 3 · Choosing your destination — a 5-step framework

1. **Define your medical need.** Cosmetic → Turkey, Thailand, South Korea. Cardiac/oncology → Germany, Switzerland. IVF → Turkey, Malaysia, Spain. Orthopaedic → Germany, Turkey, Malaysia. Dental → Turkey, Hungary. Bariatric → Mexico, Thailand, Turkey.
2. **Validate hospital accreditation.** Insist on **JCI (Joint Commission International)** plus local government registration. Check independent reviews and published revision/complication rates.
3. **Verify surgeon credentials.** Ask: *How many of this exact procedure has the surgeon performed?* (Target: 500+). Their published complication rates? Will they coordinate post-op with your Saudi physician?
4. **Confirm financial transparency.** Demand an itemised quote: surgeon fee, facility fee, anaesthesia, implants, medications, post-op imaging, transfers. Vague "package prices" are a walk-away signal.
5. **Test responsiveness.** Ask one detailed clinical question. A 24-hour, medically-detailed reply predicts the same coordination quality later.

### 4 · The 60-day pre-travel checklist

**Weeks 1–2 — Medical preparation**
- Gather all imaging (CT/MRI/X-rays with digital copies), bloodwork, medication list, allergy history, surgical history.
- Bundle into one PDF: **MEDICAL_RECORDS_[YourName]_[Date]** and email to the foreign hospital's coordinator.
- Obtain certified medical translations (SAR 50–150 per document, 5–7 business days). For a deeper guide, see [medical document translation & AI scanning for Saudi patients](/news/medical-document-translation-ai-scanning).
- Establish baseline metrics: BP, resting HR, weight, sleep quality, current pain.

**Weeks 3–4 — Logistics**
- Flights: 1–2 days pre-op (SAR 2,000–4,000 economy).
- Accommodation: 14–21 nights (SAR 100–300/night recovery hotel).
- **Bring a caregiver.** Non-negotiable for any major procedure.
- Meet your Saudi physician *in person* and request a written **clinical summary** for the foreign team.
- Notify your insurance — confirm coverage and **repatriation clause** in writing. Most Saudi insurance does *not* cover elective tourism. Plan to self-pay.

**Weeks 5–6 — Final confirmation**
- Pre-op bloodwork (often locally in KSA, emailed 1 week before departure).
- Medication adjustments (stop aspirin? continue beta-blockers?).
- Pre-order post-op medications at your Saudi pharmacy *before* departure.
- Print a physical folder: passport, medical visa, insurance, hospital + Saudi-doctor contacts, records, medications, allergies.

### 5 · During treatment — coordination & communication

**Day 1 (arrival).** Confirm pre-op labs/imaging. Meet surgeon, anaesthesiologist, nursing coordinator. Ask three critical questions:
- *"Will you send my discharge summary directly to my Saudi physician?"*
- *"Who do I contact after discharge?"*
- *"What's your post-op protocol?"*

**Day before surgery.** Finalise medication stops/starts. Meet anaesthesiologist — discuss allergies and family anaesthesia history. Final briefing on incision location and recovery timeline.

**Days 1–7 post-op.** Don't suffer pain in silence. Watch incisions for redness/warmth/discharge. Follow physical-therapy mobility rules *exactly*. Photograph the incision daily, log pain scores and medication times — this becomes your discharge summary.

**Pre-discharge (Day 5–7).** Obtain the **complete discharge summary**, prescriptions for ≥2 weeks, follow-up schedule (2 weeks → 6 weeks → 3 months), and the surgeon's commitment to remote follow-up.

### 6 · The critical return — coordinating with your Saudi physician

This is where the system breaks. You return home post-op, exhausted and fragile — and your Saudi physician has no insight into what was done. **Solution: a structured handoff** — see our deep-dive on [recovery coordination & post-treatment care](/news/recovery-coordination-post-treatment-care).

**Week 1 in Saudi Arabia.** Schedule an in-person or video visit with your Saudi physician. Bring the discharge summary (translated), surgical photos/videos if provided, current medications, foreign bloodwork, activity restrictions, and the foreign surgeon's contact.

Discuss:
1. **Medication reconciliation** — interactions with your chronic medications.
2. **Activity timeline** — when can you resume work, driving, exercise?
3. **Symptom expectations** — what swelling/pain/stiffness is normal?
4. **Emergency protocols** — fever, sudden pain, drainage → action plan.
5. **Joint follow-up** — coordinated 6-week and 3-month imaging with the foreign surgeon.

**Recovery arc.** Weeks 1–2 = acute (pain, swelling, fatigue, scab is normal). Weeks 2–4 = mobilisation (swelling peaks day 10 then drops, PT intensifies, light activities resume).

**The handoff protocol.** Before your foreign surgeon goes offline, request: a written post-op protocol, an imaging schedule, explicit authority for your Saudi physician to act, and a virtual follow-up calendar (2 weeks → 6 weeks → 3 months → as needed). This transforms a *handoff* into a **partnership**.

