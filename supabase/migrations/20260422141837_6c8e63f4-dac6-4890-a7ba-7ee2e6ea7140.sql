### The three translation failure modes

**1. Linguistic incompetence.** Word-for-word translation by non-medical translators. "Malignancy" becomes "wickedness." Clinical meaning is lost.

**2. Jurisdictional incompatibility.** A Saudi PSA result of "12 ng/mL" means nothing to a German urologist without the reference range "<4.0 ng/mL." Without context, they order redundant testing.

**3. Cultural-medical assumption gaps.** Diagnostic codes differ. Saudi doctors write clinical narratives. German systems expect structured data fields. Hospitals end up re-diagnosing to confirm.

