تشير بيانات السياحة العلاجية إلى أن **163,000 مريض سعودي** يسافرون للعلاج سنوياً، 58% منهم يحاولون الترجمة بأنفسهم، و1 من كل 3 يواجه تأخيراً في الجراحة بسبب أخطاء الترجمة، بمتوسّط 7–14 يوماً وتكلفة إضافية 5,000–15,000 ريال.

للسياق الكامل لرحلة العلاج، اقرأ [الدليل الشامل للسياحة العلاجية للمرضى السعوديين](/ar/news/medical-tourism-saudi-patients-2026)، ولمرحلة ما بعد الجراحة اقرأ [دليل تنسيق التعافي والمتابعة](/ar/news/recovery-coordination-post-treatment-care).

### الخلاصة

الترجمة الطبية ليست اختيارية — إنها أساس الرعاية. استخدم الطريقة الهجينة، نسّق مع طبيبك السعودي، وأرسل وثائقك منظّمة. هذا ليس معقّداً، لكنه يحتاج إلى نية واضحة.

